module.exports = class Parent {
    constructor(x, y){
        this.x = x;
        this.y = y;
        this.multiply = 0;
    }
    newDirections() {
        this.directions = [
            [this.x - 1, this.y - 1],
            [this.x, this.y - 1],
            [this.x + 1, this.y - 1],
            [this.x - 1, this.y],
            [this.x + 1, this.y],
            [this.x - 1, this.y + 1],
            [this.x, this.y + 1],
            [this.x + 1, this.y + 1]
        ];
    }
    random(arr){
        let a = arr[Math.floor( Math.random() * arr.length)];
        return a;
    }
}